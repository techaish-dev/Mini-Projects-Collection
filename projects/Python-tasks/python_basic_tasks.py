import socket
import threading
import concurrent.futures

# Function to scan a single port on the target
def scan_port(target, port):
    try:
        # Create a socket object
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
            # Set a timeout for the connection attempt
            s.settimeout(1)
            # Attempt to connect to the target's IP address and port
            result = s.connect_ex((target, port))
            if result == 0:
                # Banner grabbing to retrieve service information
                banner = s.recv(1024).decode().strip()
                print(f"Port {port} is open. Service: {banner}")
    except KeyboardInterrupt:
        print("Scanning stopped by user.")
        exit()
    except socket.gaierror:
        print("Hostname could not be resolved.")
        exit()
    except socket.error:
        pass

# Multi-threaded port scanning
def scan_ports(target, port_range, num_threads):
    print(f"Scanning target {target}...")
    with concurrent.futures.ThreadPoolExecutor(max_workers=num_threads) as executor:
        for port in range(port_range[0], port_range[1] + 1):
            executor.submit(scan_port, target, port)

# Main function
def main():
    try:
        target = input("Enter target IP address or domain: ")
        start_port = int(input("Enter start port: "))
        end_port = int(input("Enter end port: "))
        num_threads = int(input("Enter number of threads (e.g., 10): "))

        if start_port > end_port:
            print("Start port cannot be greater than end port.")
            return

        port_range = (start_port, end_port)

        scan_ports(target, port_range, num_threads)

    except KeyboardInterrupt:
        print("Scanning stopped by user.")
        exit()
    except ValueError:
        print("Invalid input. Please enter a valid number.")
        exit()

if __name__ == "__main__":
    main()

