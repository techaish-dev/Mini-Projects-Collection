"""##OpenCV:

Description: A computer vision library that provides tools for image and video analysis, processing, and manipulation.
Example: Create a script to detect and track objects in a live video stream.
"""

import cv2
import numpy as np
from google.colab.patches import cv2_imshow

# Initialize video capture from a sample video URL
video_url = "https://www.sample-videos.com/video123/mp4/720/big_buck_bunny_720p_10mb.mp4"
cap = cv2.VideoCapture(video_url)

# Define the lower and upper bounds of the red color in HSV format
lower_red = np.array([0, 100, 100])
upper_red = np.array([10, 255, 255])

# Create a kernel for morphological operations (used for noise reduction)
kernel = np.ones((5, 5), np.uint8)

while True:
    # Read a frame from the video
    ret, frame = cap.read()

    if not ret:
        break

    # Convert the frame to the HSV color space
    hsv = cv2.cvtColor(frame, cv2.COLOR_BGR2HSV)

    # Create a mask for the red color
    mask = cv2.inRange(hsv, lower_red, upper_red)

    # Apply morphological operations to the mask (noise reduction)
    mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN, kernel)
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel)

    # Find contours in the mask
    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)

    # Draw bounding boxes around detected objects
    for contour in contours:
        area = cv2.contourArea(contour)
        if area > 100:  # Adjust the area threshold as needed
            x, y, w, h = cv2.boundingRect(contour)
            cv2.rectangle(frame, (x, y), (x + w, y + h), (0, 255, 0), 2)

    # Display the processed frame
    cv2_imshow(frame)

    # Break the loop when 'q' is pressed
    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

# Release the video capture
cap.release()

