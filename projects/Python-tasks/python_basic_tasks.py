import pygame
import random

# Initialize Pygame
pygame.init()

# Set up game constants
WIDTH, HEIGHT = 800, 600
PLAYER_SIZE = 50
PLAYER_SPEED = 5
WHITE = (255, 255, 255)

# Create the game window
screen = pygame.display.set_mode((WIDTH, HEIGHT))
pygame.display.set_caption("Simple Pygame Game")

# Define the player character
player = pygame.Surface((PLAYER_SIZE, PLAYER_SIZE))
player.fill((0, 128, 255))
player_rect = player.get_rect()
player_rect.center = (WIDTH // 2, HEIGHT // 2)

# Create an enemy character
enemy = pygame.Surface((PLAYER_SIZE, PLAYER_SIZE))
enemy.fill((255, 0, 0))
enemy_rect = enemy.get_rect()
enemy_rect.center = (random.randint(0, WIDTH), random.randint(0, HEIGHT))

# Set up a clock to control the game's frame rate
clock = pygame.time.Clock()

# Main game loop
running = True
while running:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False

    # Handle player input
    keys = pygame.key.get_pressed()
    if keys[pygame.K_LEFT]:
        player_rect.x -= PLAYER_SPEED
    if keys[pygame.K_RIGHT]:
        player_rect.x += PLAYER_SPEED
    if keys[pygame.K_UP]:
        player_rect.y -= PLAYER_SPEED
    if keys[pygame.K_DOWN]:
        player_rect.y += PLAYER_SPEED

# Check for collisions
    if player_rect.colliderect(enemy_rect):
        enemy_rect.center = (random.randint(0, WIDTH), random.randint(0, HEIGHT))

# Fill the screen with white
    screen.fill(WHITE)

# Draw the player and enemy characters
    screen.blit(player, player_rect)
    screen.blit(enemy, enemy_rect)

# Update the display
    pygame.display.update()

# Cap the frame rate
    clock.tick(60)

# Quit Pygame
pygame.quit()

